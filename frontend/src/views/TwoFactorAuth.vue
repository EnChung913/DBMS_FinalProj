<script setup lang="ts">
import { ref } from 'vue';
import { useRouter } from 'vue-router';
import apiClient from '@/api/axios'; // ÂÅáË®≠‰Ω†ÁöÑ axios ÂØ¶‰æã‰ΩçÁΩÆ
import { useAuthStore } from '@/stores/auth';

const router = useRouter();
const authStore = useAuthStore();

// UI ÁãÄÊÖãÊéßÂà∂
const step = ref<1 | 2 | 3>(1); // 1: ÂàùÂßã, 2: ÊéÉÊèèËàáËº∏ÂÖ•, 3: ÊàêÂäü
const isLoading = ref(false);
const errorMsg = ref('');

// Ë≥áÊñôÊö´Â≠ò
const qrCodeUrl = ref('');
const secretKey = ref('');
const verificationCode = ref('');

/**
 * Step 1 -> Step 2: ÂëºÂè´ÂæåÁ´ØÁî¢Áîü Secret & QR Code
 */
const startSetup = async () => {
  isLoading.value = true;
  errorMsg.value = '';
  
  try {
    // Ê≥®ÊÑèÔºöÈÄôË£°ÁöÑË∑ØÂæëÂèñÊ±∫Êñº‰Ω†ÁöÑ Controller ÂâçÁ∂¥ÔºåÂÅáË®≠ÊòØ /api/auth/2fa/generate
    // Â¶ÇÊûú‰Ω†ÁöÑ Controller Ê≤íÊúâ auth ÂâçÁ∂¥ÔºåË´ãÊîπÁÇ∫ /api/2fa/generate
    const res = await apiClient.post('/api/auth/2fa/generate');
    
    // ÂÅáË®≠ÂæåÁ´ØÂõûÂÇ≥Ê†ºÂºèÁÇ∫ { secret: string, qrCode: string (DataURI) }
    // Â¶ÇÊûúÂæåÁ´ØÊòØÁî® otplibÔºåÈÄöÂ∏∏ÊúÉÂõûÂÇ≥ÈÄôÂÖ©ÂÄãÊ¨Ñ‰Ωç
    secretKey.value = res.data.secret;
    qrCodeUrl.value = res.data.qr; // ÂøÖÈ†àÊòØ data:image/png;base64,... Ê†ºÂºè
    
    step.value = 2;
  } catch (err: any) {
    console.error('Failed to generate 2FA:', err);
    errorMsg.value = err.response?.data?.message || 'Cannot generate 2FA setup, please try again later.';
  } finally {
    isLoading.value = false;
  }
};

/**
 * Step 2 -> Step 3: ÈÄÅÂá∫È©óË≠âÁ¢ºÂïüÁî® 2FA
 */
const verifyAndEnable = async () => {
  if (!verificationCode.value || verificationCode.value.length !== 6) {
    errorMsg.value = 'Please enter a 6-digit verification code';
    return;
  }

  isLoading.value = true;
  errorMsg.value = '';

  try {
    await apiClient.post('/api/auth/2fa/enable', {
      code: verificationCode.value
    });

    step.value = 3;
    // Â¶ÇÊûú store ÊúâÁ¥ÄÈåÑ user ÁãÄÊÖãÔºåÂèØ‰ª•Âú®ÈÄôË£°Êõ¥Êñ∞Ôºå‰æãÂ¶ÇÔºö
    authStore.set2FAEnabled(true); 
  } catch (err: any) {
    console.error('Failed to enable 2FA:', err);
    errorMsg.value = 'Verification code is incorrect or expired, please try again.';
  } finally {
    isLoading.value = false;
  }
};

/**
 * ÂÆåÊàêÂæåËøîÂõû Dashboard
 */
const finishSetup = () => {
  
  router.push('/student/dashboard');
};

/**
 * Ë§áË£ΩÂØÜÈë∞Âà∞Ââ™Ë≤ºÁ∞ø (UX ÂÑ™Âåñ)
 */
const copySecret = () => {
  navigator.clipboard.writeText(secretKey.value);
  alert('Secret key copied to clipboard!');
};
</script>

<template>
  <div class="auth-wrapper">
    <div class="auth-card">
      <header class="card-header">
        <div class="icon-shield">üõ°Ô∏è</div>
        <h2>Two-Factor Authentication (2FA) Setup</h2>
      </header>

      <div v-if="errorMsg" class="error-banner">
        {{ errorMsg }}
      </div>

      <div v-if="step === 1" class="step-content">
        <p class="description">
          Enabling two-factor authentication significantly enhances your account security.
          <br>After setup, you will need to enter a verification code generated by Google Authenticator in addition to your password when logging in.
        </p>
        <button class="btn-primary" @click="startSetup" :disabled="isLoading">
          {{ isLoading ? 'Processing...' : 'Start Setup' }}
        </button>
        <button class="btn-text" @click="router.back()">Maybe Later</button>
      </div>

      <div v-else-if="step === 2" class="step-content">
        <div class="qr-section">
          <p class="instruction">1. Please use Google Authenticator to scan the QR Code below</p>
          
          <div class="qr-img-wrapper">
            <img v-if="qrCodeUrl" :src="qrCodeUrl" alt="2FA QR Code" />
            <div v-else class="loading-placeholder">Loading...</div>
          </div>

          <div class="secret-text">
            <span>Or manually enter the secret key:</span>
            <code @click="copySecret" title="Click to copy">{{ secretKey }}</code>
          </div>
        </div>

        <div class="input-section">
          <p class="instruction">2. Enter the 6-digit verification code from the app</p>
          <input 
            v-model="verificationCode" 
            type="text" 
            maxlength="6" 
            placeholder="000 000" 
            class="code-input"
            @keyup.enter="verifyAndEnable"
          />
        </div>

        <div class="action-buttons">
          <button class="btn-primary" @click="verifyAndEnable" :disabled="isLoading">
            {{ isLoading ? 'Verifying...' : 'Enable Protection' }}
          </button>
          <button class="btn-secondary" @click="step = 1" :disabled="isLoading">
            Previous
          </button>
        </div>
      </div>

      <div v-else-if="step === 3" class="step-content success-step">
        <div class="success-icon">‚úÖ</div>
        <h3>Setup Complete!</h3>
        <p>Your account is now protected by two-factor authentication.</p>
        <button class="btn-primary" @click="finishSetup">
          Back to Dashboard
        </button>
      </div>

    </div>
  </div>
</template>

